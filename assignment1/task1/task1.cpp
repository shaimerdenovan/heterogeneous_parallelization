/*Задание 1
Реализуйте программу на C++, которая динамически выделяет массив из 50 000 целых
чисел, заполняет его случайными значениями от 1 до 100, вычисляет среднее значение
элементов массива и корректно освобождает выделенную память. */

/*подключаем библиотеку для ввода и вывода данных*/
#include <iostream>                 
/*библиотека для генерации случайных чисел*/
#include <random>                  

/*используем стандартное пространство имён чтоб не писать std:: каждый раз*/
using namespace std;               

/*главная функция*/
int main() {                        
    /*размер массива устанавливаем 50000 элементов*/
    const int SIZE = 50000;         
    /*динамически выделяем память под массив*/
    int* array = new int[SIZE];       

    /*создаем источник случайных чисел*/
    random_device rd;              
    /*генератор случайных чисел*/
    mt19937 gen(rd());              
    /*задаем диапазон от 1 до 100*/
    uniform_int_distribution<int> dist(1, 100); 

    /*переменная для хранения суммы всех элементов массива*/
    long long totalsum = 0;             

    /*заполняем массив случайными числами*/
    for (int i = 0; i < SIZE; i++) {
        /*записываем случайное число*/
        array[i] = dist(gen);         
        /*добавляем элемент к сумме*/
        totalsum += array[i];              
    }                               

    /*этап вычисления среднего значения*/
    double avgvalue = (double)totalsum / SIZE; 

    /*вывод среднего значения*/
    cout << "The average value is " << avgvalue << endl; 

    /*освобождаем выделенную память*/
    delete[] array;                  
    /*обнуляем указатель*/
    array = nullptr;                  

    /*завершение программы*/
    return 0;                       
}