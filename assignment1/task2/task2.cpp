/*Задание 2
Создайте массив из 1 000 000 целых чисел и реализуйте последовательный алгоритм
поиска минимального и максимального элементов. Замерьте время выполнения алгоритма.*/

/*подключаем библиотеку для ввода и вывода данных*/
#include <iostream>
/*библиотека для генерации случайных чисел*/
#include <random>
/*библиотека для измерения времени выполнения*/
#include <chrono>
/*библиотека для получения минимальных и максимальных значений*/
#include <limits>

/*используем стандартное пространство имён чтоб не писать std:: каждый раз*/
using namespace std;

/*главная функция*/
int main() {

    /*размер массива устанавливаем 1000000 элементов*/
    const int SIZE = 1000000;
    /*динамически выделяем память под массив целых чисел*/
    int* array = new int[SIZE];

    /*создаем источник случайных чисел*/
    random_device rd;
    /*генератор случайных чисел*/
    mt19937 gen(rd());
    /*задаем диапазон от 1 до 100*/
    uniform_int_distribution<int> dist(1, 100);

    /*заполняем массив случайными числами*/
    for (int i = 0; i < SIZE; i++) {
        /*записываем случайное число*/
        array[i] = dist(gen);
    }

    /*переменная для хранения минимального значения*/
    int min_value = numeric_limits<int>::max();
    /*переменная для хранения максимального значения*/
    int max_value = numeric_limits<int>::min();

    /*запоминаем время начала поиска*/
    auto start = chrono::high_resolution_clock::now();

    /*последовательно ищем минимальный и максимальный элементы массива*/
    for (int i = 0; i < SIZE; i++) {
        if (array[i] < min_value) {
            /*обновляем минимальное значение*/
            min_value = array[i];
        }

        if (array[i] > max_value) {
            /*обновляем максимальное значение*/
            max_value = array[i];
        }
    }

    /*запоминаем время окончания поиска*/
    auto end = chrono::high_resolution_clock::now();

    /*вычисляем затраченное время в секундах*/
    chrono::duration<double> elapsed = end - start;

    /*выводим минимальное значение*/
    cout << "The minimum value is: " << min_value << endl;
    /*выводим максимальное значение*/
    cout << "The maximum value is: " << max_value << endl;
    /*выводим время выполнения программы*/
    cout << "The time in seconds is: " << elapsed.count() << endl;

    /*освобождаем динамически выделенную память*/
    delete[] array;

    /*обнуляем указатель*/
    array = nullptr;

    /*завершение программы*/
    return 0;
}